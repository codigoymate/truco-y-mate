int main(void) {
	int bg, fg, p = 1;

    initscr();
    start_color();

    /* Init color pairs */
    for (bg = 0; bg < 8; bg ++) {
        for (fg = 0; fg < 8; fg ++) {
            init_pair(p, fg, bg); p ++;
        }
    }

    draw(stdscr);
    refresh();
    getch();
    endwin();

    return 0;
}

